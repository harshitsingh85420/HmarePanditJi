/**
 * Antigravity Desktop Automation
 * 
 * This script automates Google Antigravity desktop app using Puppeteer
 * to run tests and save results for Qwen Code to fix
 * 
 * Prerequisites:
 * 1. Install Antigravity from: https://antigravity.google/download
 * 2. Configure Antigravity with your preferred AI models
 * 
 * Usage:
 *   node scripts/run-antigravity-tests.js "Find all bugs"
 *   node scripts/run-antigravity-tests.js --test
 *   node scripts/run-antigravity-tests.js --review
 */

const { execSync, spawn } = require('child_process');
const fs = require('fs');
const path = require('path');

const RESULTS_DIR = path.join(__dirname, '..', 'test-results');
const TIMESTAMP = new Date().toISOString().replace(/[:.]/g, '-').slice(0, -5);

// Ensure results directory exists
if (!fs.existsSync(RESULTS_DIR)) {
  fs.mkdirSync(RESULTS_DIR, { recursive: true });
}

function log(message, type = 'info') {
  const prefix = type === 'error' ? '❌' : type === 'success' ? '✅' : '📝';
  console.log(`${prefix} ${message}`);
}

/**
 * Run Antigravity via command line if available
 * Falls back to creating a task file for manual execution
 */
function runAntigravity(prompt, options = {}) {
  const { 
    timeout = 300000 
  } = options;

  const resultFile = path.join(RESULTS_DIR, `antigravity-${TIMESTAMP}.md`);

  log(`Starting Antigravity with prompt: "${prompt}"`);

  // Try to run antigravity CLI if available
  try {
    // Check if antigravity command exists
    execSync('antigravity --version', { stdio: 'pipe' });
    
    log('Antigravity CLI found, running...');
    
    const result = execSync(`antigravity run --prompt "${prompt}" --output "${resultFile}"`, {
      encoding: 'utf-8',
      timeout,
      stdio: ['pipe', 'pipe', 'pipe']
    });

    log('Antigravity completed successfully', 'success');
    return {
      success: true,
      resultFile
    };

  } catch (error) {
    // CLI not available, create task file
    log('Antigravity CLI not found, creating task file...', 'info');

    const taskFile = path.join(RESULTS_DIR, `task-${TIMESTAMP}.md`);
    const taskContent = `# Antigravity Test Task

**Created:** ${new Date().toISOString()}
**Prompt:** ${prompt}

---

## Instructions for Manual Execution

1. Open Google Antigravity (antigravity.google or desktop app)
2. Create a new agent/session
3. Paste the following prompt:

\`\`\`
${prompt}
\`\`\`

4. Let the agent run and complete the analysis
5. Copy the agent's output and save it to: \`${resultFile}\`
6. Tell Qwen Code: "Check test-results/ and fix the issues"

---

## Alternative: Use Gemini CLI

If you have Gemini CLI installed:

\`\`\`bash
npm install -g @google/gemini-cli
gemini auth
gemini -p "${prompt}" --output-format json > ${RESULTS_DIR}/result-${TIMESTAMP}.json
\`\`\`

---

## Prompt Details

**Type:** ${options.type || 'general'}
**Priority:** ${options.priority || 'normal'}
**Expected Output:** Code analysis, bug reports, test results

---

*This file was auto-generated by run-antigravity-tests.js*
`;

    fs.writeFileSync(taskFile, taskContent);
    
    log(`Task file created: ${taskFile}`, 'success');
    
    return {
      success: 'manual',
      taskFile,
      resultFile,
      prompt
    };
  }
}

/**
 * Create a simple test runner using existing Puppeteer setup
 */
function runBrowserTest(prompt) {
  log('Running browser-based test with Puppeteer...');
  
  const testFile = path.join(RESULTS_DIR, `browser-test-${TIMESTAMP}.md`);
  
  // Create a simple test script
  const testScript = `
const puppeteer = require('puppeteer');

(async () => {
  const browser = await puppeteer.launch({ headless: 'new' });
  const page = await browser.newPage();
  
  // Run project tests
  try {
    const { execSync } = require('child_process');
    const testOutput = execSync('pnpm test', { encoding: 'utf-8' });
    
    const report = \`# Test Results

**Date:** ${new Date().toISOString()}
**Prompt:** ${prompt}

## Test Output

\`\`\`
\${testOutput}
\`\`\`

## Analysis Request

${prompt}

---
*Generated by Puppeteer test runner*
\`;

    require('fs').writeFileSync('${testFile}', report);
    console.log('Test completed:', '${testFile}');
  } catch (error) {
    const report = \`# Test Results (Failed)

**Date:** ${new Date().toISOString()}
**Error:** \${error.message}

## Prompt

${prompt}

---
*Generated by Puppeteer test runner*
\`;

    require('fs').writeFileSync('${testFile}', report);
    console.log('Test failed, see report');
  }
  
  await browser.close();
})();
`;

  const scriptPath = path.join(RESULTS_DIR, `run-test-${TIMESTAMP}.js`);
  fs.writeFileSync(scriptPath, testScript);
  
  try {
    execSync(`node "${scriptPath}"`, { 
      encoding: 'utf-8',
      stdio: 'inherit',
      timeout: 120000
    });
    
    return {
      success: true,
      resultFile: testFile
    };
  } catch (error) {
    log(`Browser test failed: ${error.message}`, 'error');
    return {
      success: false,
      error: error.message
    };
  }
}

// Main execution
async function main() {
  const args = process.argv.slice(2);
  
  if (args.length === 0) {
    console.log(`
╔══════════════════════════════════════════════════════════╗
║         Antigravity / AI Tester Runner                   ║
╠══════════════════════════════════════════════════════════╣
║  Usage:                                                  ║
║    node scripts/run-antigravity-tests.js "<prompt>"      ║
║                                                          ║
║  Examples:                                               ║
║    node scripts/run-antigravity-tests.js "Find bugs"     ║
║    node scripts/run-antigravity-tests.js --test          ║
║    node scripts/run-antigravity-tests.js --review        ║
║                                                          ║
║  Note: Requires Google Antigravity or Gemini CLI         ║
║  Download: https://antigravity.google/download           ║
╚══════════════════════════════════════════════════════════╝
    `);
    process.exit(0);
  }

  // Parse arguments
  let prompt = '';
  const options = {};

  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--test') {
      prompt = 'Run all tests in this project. Report any failures, errors, or issues found. Be thorough.';
      options.type = 'testing';
    } else if (args[i] === '--review') {
      prompt = 'Review this codebase for code quality, security issues, performance problems, and best practices violations. Provide a detailed report.';
      options.type = 'review';
    } else if (args[i] === '--browser-test') {
      options.useBrowser = true;
    } else if (!args[i].startsWith('--')) {
      prompt = args[i];
    }
  }

  if (!prompt) {
    log('No prompt provided. Use --test, --review, or provide a custom prompt.', 'error');
    process.exit(1);
  }

  log(`Starting AI tester...`);
  
  let result;
  
  if (options.useBrowser) {
    result = runBrowserTest(prompt);
  } else {
    result = runAntigravity(prompt, options);
  }

  console.log('\n' + '='.repeat(60));
  
  if (result.success === true) {
    log('AI testing completed!', 'success');
    console.log(`
┌─────────────────────────────────────────────────────────────┐
│  NEXT STEP:                                                 │
│  Tell Qwen Code:                                            │
│  "Check ${path.relative(process.cwd(), result.resultFile)} and fix issues" │
│                                                             │
│  Or simply paste the file content in the chat.              │
└─────────────────────────────────────────────────────────────┘
    `);
  } else if (result.success === 'manual') {
    log('Manual task file created', 'success');
    console.log(`
┌─────────────────────────────────────────────────────────────┐
│  MANUAL STEP REQUIRED:                                      │
│                                                             │
│  1. Open the task file:                                     │
│     ${path.relative(process.cwd(), result.taskFile).padEnd(50)}│
│                                                             │
│  2. Follow instructions to run in Antigravity               │
│  3. Save results to test-results/                           │
│  4. Tell Qwen Code: "Check test-results/ and fix"           │
└─────────────────────────────────────────────────────────────┘
    `);
  } else {
    log('AI testing failed.', 'error');
  }
  
  console.log('='.repeat(60) + '\n');
}

main().catch(console.error);
