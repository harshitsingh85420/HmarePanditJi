/**
 * Antigravity Setup Script
 * 
 * Configures Antigravity integration for visual browser testing
 * 
 * Usage:
 *   node scripts/setup-antigravity.js
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const ROOT_DIR = path.join(__dirname, '..');
const ANTIGRAVITY_DIR = path.join(ROOT_DIR, '.antigravity');

function log(message, type = 'info') {
  const emoji = {
    info: '📝',
    success: '✅',
    error: '❌',
    warn: '⚠️'
  }[type];
  console.log(`${emoji} ${message}`);
}

function ensureDir(dir) {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
    log(`Created directory: ${dir}`);
  }
}

function checkAntigravity() {
  log('Checking Antigravity installation...');
  
  try {
    // Check if Antigravity CLI exists
    execSync('antigravity --version', { stdio: 'pipe' });
    log('Antigravity CLI found', 'success');
    return true;
  } catch {
    log('Antigravity CLI not found', 'warn');
    return false;
  }
}

function checkGeminiCLI() {
  log('Checking Gemini CLI installation...');
  
  try {
    execSync('gemini --version', { stdio: 'pipe' });
    const version = execSync('gemini --version', { encoding: 'utf-8' }).trim();
    log(`Gemini CLI found: v${version}`, 'success');
    return true;
  } catch {
    log('Gemini CLI not found', 'warn');
    return false;
  }
}

function setupMCP() {
  log('Setting up MCP servers...');
  
  const mcpConfig = {
    mcpServers: {
      puppeteer: {
        command: 'npx',
        args: ['-y', '@modelcontextprotocol/server-puppeteer']
      },
      filesystem: {
        command: 'npx',
        args: ['-y', '@modelcontextprotocol/server-filesystem', ROOT_DIR]
      }
    }
  };
  
  const mcpFile = path.join(ANTIGRAVITY_DIR, 'mcp.json');
  fs.writeFileSync(mcpFile, JSON.stringify(mcpConfig, null, 2));
  log(`MCP config saved: ${mcpFile}`, 'success');
}

