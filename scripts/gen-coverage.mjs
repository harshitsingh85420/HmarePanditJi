#!/usr/bin/env node
// ─────────────────────────────────────────────────────────────
// COVERAGE REGENERATOR — rebuilds docs/spec/coverage.yaml from a VERIFIED
// grep of the tree. An `auto` entry is emitted only when a real test file
// literally names the register ID inside a describe/it title line — the
// same contract scripts/conformance-guard.mjs enforces. PROPOSED PW- rows
// are never emitted (they stay undischarged by design).
//
//   node scripts/gen-coverage.mjs          → report (what would be written)
//   node scripts/gen-coverage.mjs --write  → rewrite docs/spec/coverage.yaml
// ─────────────────────────────────────────────────────────────
import { readFileSync, writeFileSync, readdirSync, statSync } from "node:fs";
import { join, relative } from "node:path";

const ROOT = join(import.meta.dirname, "..");
const WRITE = process.argv.includes("--write");

function walkTests(dir, out = []) {
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    const st = statSync(p);
    if (st.isDirectory()) {
      if (name === "node_modules" || name === ".next" || name === "dist") continue;
      walkTests(p, out);
    } else if (/\.test\.(ts|tsx|mjs)$/.test(name)) {
      out.push(p);
    }
  }
  return out;
}

const files = [
  ...walkTests(join(ROOT, "apps", "pandit", "src")),
  ...walkTests(join(ROOT, "services", "api", "src")),
];

// id → Set<file>. The guard's enforced contract (conformance-guard.mjs:161)
// is full-file-text `includes(id)` — reproduce EXACTLY that, no stricter,
// no looser, so this file always mirrors what the guard will accept.
const map = new Map();
for (const f of files) {
  const rel = relative(ROOT, f).replaceAll("\\", "/");
  const text = readFileSync(f, "utf8");
  for (const id of text.match(/F\d{2}-\d{2}/g) ?? []) {
    if (!map.has(id)) map.set(id, new Set());
    map.get(id).add(rel);
  }
}

const ids = [...map.keys()].sort();
const lines = [
  "# ─────────────────────────────────────────────────────────────",
  "# CONFORMANCE COVERAGE MAP — requirement id → how it is proven.",
  "# scripts/conformance-guard.mjs reads this. Rules:",
  "#   auto      → a test file in THIS tree must literally name the id",
  "#   manual    → the id must appear in docs/spec/MANUAL_TESTS.md",
  "#   deviation → discharges ONLY when SIGNED in docs/spec/DEVIATIONS.md",
  "# REGENERATED by scripts/gen-coverage.mjs from a verified grep — every",
  "# entry names a real test file whose describe/it title carries the id.",
  "# PROPOSED PW- rows are not in the enforced set and appear nowhere here.",
  "# ─────────────────────────────────────────────────────────────",
  "",
  "coverage:",
];
for (const id of ids) {
  const refs = [...map.get(id)].sort();
  lines.push(`  - id: ${id}`);
  lines.push(`    kind: auto`);
  lines.push(`    ref: ${refs[0]}`);
  for (const extra of refs.slice(1)) lines.push(`    # also: ${extra}`);
}
lines.push("");

const out = lines.join("\n");
if (WRITE) {
  writeFileSync(join(ROOT, "docs", "spec", "coverage.yaml"), out, "utf8");
  console.log(`WROTE docs/spec/coverage.yaml — ${ids.length} auto entries`);
} else {
  console.log(`${ids.length} auto entries would be written:`);
}
for (const id of ids) console.log(`  ${id} ← ${[...map.get(id)][0]}`);
