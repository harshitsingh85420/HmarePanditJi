#!/usr/bin/env node
// ─────────────────────────────────────────────────────────────
// REGISTER → requirements.yaml
//
// docs/pandit-pov-conformance-register.md is Isj's document and stays the
// human source of truth. This derives the machine-readable projection of
// it so CI can enforce what the register only asserts.
//
// TWO FILES, ON PURPOSE:
//   docs/spec/requirements.yaml  GENERATED — regenerating never loses work
//   docs/spec/coverage.yaml      HAND-MAINTAINED — the ID→proof mapping
// If both lived in one file, regenerating after Isj edits the register
// would silently wipe every mapping. They are kept apart so the register
// can evolve without destroying the evidence trail.
//
// Run:  node scripts/gen-requirements.mjs         (writes)
//       node scripts/gen-requirements.mjs --check (fails if stale — CI)
// ─────────────────────────────────────────────────────────────
import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { createHash } from "node:crypto";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const REGISTER = join(ROOT, "docs", "pandit-pov-conformance-register.md");
const OUT = join(ROOT, "docs", "spec", "requirements.yaml");

const STATUS = {
  "✅": "built",
  "🟡": "partial",
  "❌": "missing",
  "🔍": "verify",
};

/** double-quoted YAML scalar — the requirement text carries :, ", →, **, Devanagari */
const q = (s) => `"${String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;

function parseRegister(md) {
  const lines = md.split(/\r?\n/);
  const blocks = [];
  let block = null;

  for (const line of lines) {
    const h = line.match(/^##\s+(F[0-9–\-]+)\s*—\s*(.+?)\s*$/);
    if (h) {
      // strip trailing ⚠-style editorial marks from the title
      const title = h[2].replace(/\s*⚠.*$/, "").trim();
      block = { id: h[1], title, requirements: [] };
      blocks.push(block);
      continue;
    }
    const row = line.match(/^\|\s*(F\d{2}-\d{2})\s*\|(.+)$/);
    if (!row || !block) continue;

    const cells = row[2].split("|").map((c) => c.trim());
    // cells: [requirement, type, status, ""] — trailing pipe yields an empty tail
    const [requirement, type, statusCell] = cells;

    const glyph = Object.keys(STATUS).find((g) => (statusCell || "").includes(g));
    if (!glyph) throw new Error(`${row[1]}: no status glyph in ${JSON.stringify(statusCell)}`);

    // "AUTO (api contract)" / "AUTO + MANUAL" / "DEVIATION candidate"
    const kinds = [];
    if (/AUTO/i.test(type)) kinds.push("auto");
    if (/MANUAL/i.test(type)) kinds.push("manual");
    if (/DEVIATION/i.test(type)) kinds.push("deviation");
    if (!kinds.length) throw new Error(`${row[1]}: unrecognised type ${JSON.stringify(type)}`);

    block.requirements.push({
      id: row[1],
      requirement,
      // the register's own words on HOW it should be proven
      testKind: kinds,
      typeRaw: type,
      status: STATUS[glyph],
      // the register sometimes annotates the status cell ("🟡 upload built",
      // "🔍 (GoOnline exists — verify rule)") — that nuance is worth keeping
      statusNote: statusCell.replace(glyph, "").trim(),
    });
  }
  return blocks;
}

function emit(blocks, sourceSha) {
  const all = blocks.flatMap((b) => b.requirements);
  const tally = all.reduce((a, r) => ((a[r.status] = (a[r.status] || 0) + 1), a), {});

  const L = [];
  L.push("# GENERATED by scripts/gen-requirements.mjs — DO NOT HAND-EDIT.");
  L.push("# Edit docs/pandit-pov-conformance-register.md, then regenerate.");
  L.push("# Coverage mappings live in coverage.yaml so they survive regeneration.");
  L.push("meta:");
  L.push(`  source: ${q("docs/pandit-pov-conformance-register.md")}`);
  L.push(`  sourceSha256: ${q(sourceSha)}`);
  L.push(`  totalRequirements: ${all.length}`);
  L.push("  tally:");
  for (const k of ["built", "partial", "missing", "verify"]) {
    L.push(`    ${k}: ${tally[k] || 0}`);
  }
  L.push("blocks:");
  for (const b of blocks) {
    L.push(`  - id: ${q(b.id)}`);
    L.push(`    title: ${q(b.title)}`);
    L.push("    requirements:");
    for (const r of b.requirements) {
      L.push(`      - id: ${q(r.id)}`);
      L.push(`        status: ${q(r.status)}`);
      if (r.statusNote) L.push(`        statusNote: ${q(r.statusNote)}`);
      L.push(`        testKind: [${r.testKind.map(q).join(", ")}]`);
      L.push(`        typeRaw: ${q(r.typeRaw)}`);
      L.push(`        requirement: ${q(r.requirement)}`);
    }
  }
  return L.join("\n") + "\n";
}

const md = readFileSync(REGISTER, "utf-8");
const sha = createHash("sha256").update(md).digest("hex").slice(0, 16);
const blocks = parseRegister(md);
const out = emit(blocks, sha);

const count = blocks.reduce((n, b) => n + b.requirements.length, 0);
if (count === 0) {
  console.error("gen-requirements: parsed ZERO requirements — the register format changed.");
  process.exit(1);
}

if (process.argv.includes("--check")) {
  const current = existsSync(OUT) ? readFileSync(OUT, "utf-8") : "";
  if (current !== out) {
    console.error(
      "✗ requirements.yaml is STALE — the register changed but the projection was not regenerated.\n" +
        "  Run: node scripts/gen-requirements.mjs"
    );
    process.exit(1);
  }
  console.log(`✓ requirements.yaml is in sync (${count} requirements across ${blocks.length} blocks)`);
} else {
  mkdirSync(dirname(OUT), { recursive: true });
  writeFileSync(OUT, out, "utf-8");
  console.log(`✓ wrote ${count} requirements across ${blocks.length} blocks → docs/spec/requirements.yaml`);
}
